"""
Font converter for Telegram bot
Converts regular text to various Unicode font styles
"""


# Monospace uppercase mapping (small caps)
MONOSPACE_UPPERCASE = {
    'A': 'ᴀ', 'B': 'ʙ', 'C': 'ᴄ', 'D': 'ᴅ', 'E': 'ᴇ', 'F': 'ғ', 'G': 'ɢ',
    'H': 'ʜ', 'I': 'ɪ', 'J': 'ᴊ', 'K': 'ᴋ', 'L': 'ʟ', 'M': 'ᴍ', 'N': 'ɴ',
    'O': 'ᴏ', 'P': 'ᴘ', 'Q': 'ǫ', 'R': 'ʀ', 'S': 's', 'T': 'ᴛ', 'U': 'ᴜ',
    'V': 'ᴠ', 'W': 'ᴡ', 'X': 'x', 'Y': 'ʏ', 'Z': 'ᴢ'
}


def to_monospace_uppercase(text: str) -> str:
    """
    Convert text to monospace uppercase (small caps) style.
    Example: "Hello World" -> "ʜᴇʟʟᴏ ᴡᴏʀʟᴅ"
    """
    result = []
    for char in text:
        if char.upper() in MONOSPACE_UPPERCASE:
            result.append(MONOSPACE_UPPERCASE[char.upper()])
        else:
            result.append(char)
    return ''.join(result)


